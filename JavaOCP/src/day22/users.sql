use mydb;
create table if not exists users(
	id int auto_increment primary key, -- id序號
    username varchar(50) unique not null, -- 名稱
    gender enum('M', 'F'), -- 性別
    birthday date, -- 生日
    interests varchar(255), -- 興趣
    self_intro varchar(255), -- 自我介紹
    appearance varchar(255) -- 外觀描述
);

