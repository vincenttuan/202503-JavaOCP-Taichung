use mydb;
select * from users;
select id, username, gender, birthday, interests, self_intro, appearance from users;
select gender, username, birthday from users order by birthday asc; -- asc 預設(小->大)
select gender, username, birthday from users order by birthday desc; -- desc (大->小)
-- 加入臨時欄位 age
select gender, username, birthday, timestampdiff(year, birthday, curdate()) as age 
from users
order by age asc, birthday desc;
-- 加入臨時欄位 average_age
select avg(timestampdiff(year, birthday, curdate())) as average_age from users;
-- 男生平均年齡
select avg(timestampdiff(year, birthday, curdate())) as average_age 
from users
where gender = 'M';
-- 男生/女生平均年齡
select gender, avg(timestampdiff(year, birthday, curdate())) as average_age 
from users
group by gender;
-- 喜歡"賽車"的是
select id, username, gender, birthday, interests, self_intro, appearance 
from users
where interests like '%賽車%';


